# Exercise 2.2: String Report
string = "The quick brown fox jumps over the lazy dog"

print(string.lower())
print(string.upper())
print(string.title())
print("Number of vowels: ", string.count('a') + string.count('e') +
      string.count('o') + string.count('i') + string.count('u'))
print("Index of z: ", string.index('z'))
